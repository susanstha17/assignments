// If it is, print "This is a fruit I like". Otherwise, print "I don't like this fruit".
void main() {
  String fruit = "orange";

  if (fruit == "apple" || fruit == "banana") {
    print("This is a fruit I like");
  } else {
    print("I don't like this fruit");
  }
}
